package dataType;
public class IntegerLiteralExample {

    public static void main(String[] args) {

        int a = 10;     // decimal
        int b = 010;    // octal
        int c = 0x10;   // hexadecimal
        int d = 0b10;   // binary

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}